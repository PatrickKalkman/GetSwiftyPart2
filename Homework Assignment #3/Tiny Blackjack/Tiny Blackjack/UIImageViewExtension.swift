import UIKit

extension UIImageView {

    func setOrigin(_ originToSet: CGPoint) {
        self.frame.origin = originToSet
    }

}

extension UILabel {

    func setOrigin(_ originToSet: CGPoint) {
        self.frame.origin = originToSet
    }

}
